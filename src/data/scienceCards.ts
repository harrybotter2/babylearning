/**
 * 指定した日のサイエンスカードを返す。存在しない場合は null。
 */
export function getScienceCardForDay(day: number): ScienceCard | null {
  return cardMap.get(day) ?? null;
}

/**
 * 指定した日のメッセージを返す。
 * サイエンスカードがある日はそのタイトル、ない日はフェーズ別応援メッセージをローテーションで返す。
 */
export function getDailyMessage(day: number, phase: string): string {
  const card = cardMap.get(day);
  if (card) return card.title;
  const messages = ENCOURAGEMENT_MESSAGES[phase] ?? ENCOURAGEMENT_MESSAGES.recognition;
  return messages[(day - 1) % messages.length];
}
